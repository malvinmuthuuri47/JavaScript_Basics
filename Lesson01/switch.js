// switch keyword

let greet = 'night';

switch(greet) {
    case 'morning':
        console.log("Good Morning!");
        break;
    case 'afternoon':
        console.log("Good Afternoon");
        break;
    case 'evening':
        console.log("Good Evening");
        break;
    case 'night':
        console.log("Good night");
        break;
    default:
        console.log("Hello!");
        break;
}